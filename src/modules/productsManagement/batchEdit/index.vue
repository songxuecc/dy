        this.loading = false
        if (result) {
          this.save({previewDeleteGoodsIds: []})

          const el = this.iconList.find(item => item.index === this.editType)
          const tableRefName = el.tableRef

          this.$refs[tableRefName] && this.$refs[tableRefName].openVisible()
        }
      }
    },
    async continueIdProductList () {
      this.loading = true
      const lostGoodsIdsSet = new Set(this.lostGoodsIds)
      const goodsIds = this.goods_ids.split(/[\s\n]/).filter(item => item).map(item => item.trim())
      const goodsIdsSet = [...new Set(goodsIds)]
      const unionSets = goodsIdsSet.filter(item => !lostGoodsIdsSet.has(item))

      const extJson = this.getEditJson()
      if (!extJson) {
        this.loading = false
        return false
      }

      let filters = {
        task_type: 1,
        task_sub_type: this.editType,
        goods_id_list: JSON.stringify(unionSets.length ? unionSets : ''),
        ext_json: JSON.stringify(this.getEditJson())
      }
      const result = await this.setFilterHhTaskProductOverview({
        filters
      })
      this.loading = false
      if (result) {
        this.save({previewDeleteGoodsIds: []})

        const el = this.iconList.find(item => item.index === this.editType)
        const tableRefName = el.tableRef

        this.$refs[tableRefName] && this.$refs[tableRefName].openVisible()
      }
    },
    // 当按商品选择时 预览
    async previewProductList () {
      if (!this.selectIds.length) {
        this.$message.error('请选择需要修改的商品')
      } else {
        this.visible = true
        this.loading = true
        const extJson = this.getEditJson()
        if (!extJson) {
          this.loading = false
          return false
        }

        let filters = {
          task_type: 1,
          task_sub_type: this.editType,
          goods_id_list: JSON.stringify(this.selectIds),
          ext_json: JSON.stringify(this.getEditJson())
        }

        await this.setFilterHhTaskProductOverview({
          filters,
          pagination: {
            page_index: 1
          }
        })
        this.save({previewDeleteGoodsIds: []})
        this.loading = false
        const el = this.iconList.find(item => item.index === this.editType)
        const tableRefName = el.tableRef

        this.$refs[tableRefName] && this.$refs[tableRefName].openVisible()
      }
    },
    // 选择修改的商品
    chooseProductList () {
      this.$refs.TableSelectProduct && this.$refs.TableSelectProduct.open()
    },
    async previewTableSelectProduct (ids, needPreview) {
      this.selectIds = ids
    },
    deleteSelectProduct (id) {
      if (this.selectIds.length) return false
      this.selectIds = this.selectIds.filter(item => item !== id)
    },
    toggleEditRecordTip () {
      this.showEditRecordTip = !this.showEditRecordTip
    },
    clearCategory () {
      this.categorys = []
    },
    chooseCategory (idx, category) {
      this.categoryVislble = true
      // const category = this.category
      if (category) {
        this.categoryActiveIdx = idx
        this.category = category
      }
      if (category && category.id && category.id !== 0) {
        this.$refs.categorySelectView.initCate(category.id, category.name)
      }
    },
    handleClose (category) {
      this.categorys.splice(this.categorys.indexOf(category), 1)
    },
    onChangeCate (data) {
      const currentCategory = this.categorys.map(item => item.name)
      if (currentCategory.includes(data.name)) {
        return this.$message.warning('已存在，请重新选择')
      }
      this.categoryVislble = false
      const arr = ['', 'success', 'info', 'warning', 'danger']
      let category = {}
      this.l = true
      if (this.category && this.categoryActiveIdx > -1) {
        category = this.category
        category = data
        category.type = this.category.type
        this.$nextTick(() => {
          this.$set(this.categorys, this.categoryActiveIdx, category)
          this.categoryActiveIdx = -1
          this.category = undefined
          this.l = false
        })
        return
      }
      this.l = false
      this.categorys.push({
        ...data,
        type: arr[this.categorys.length % 5]
      })
    }
  }

}
